public class Example09 {
    public static void main(String[] args) {
        int grade = 75;
        if (grade > 80) {
            System.out.println(grade + " is excellent");
        } else if (grade > 70) {
            System.out.println(grade + " is good");
        } else if (grade > 60) {
            System.out.println(grade + " is pass");
        } else {
            System.out.println(grade + "is fail");
        }
    }
}
